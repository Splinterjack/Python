# Задание-1:
# Дан список, заполненный произвольными целыми числами.
# Получить новый список, элементы которого будут
# квадратами элементов исходного списка
# [1, 2, 4, 0] --> [1, 4, 16, 0]

'''Функция для возведения строки в необходимую степень.
На вход подается строка и необходимая степень.'''
def mult (string,x):                    #Входящая строка
    return [i**x for i in string]       #Возведение в необходимую степень
print (mult ([6, 3, 4, 0],2))

