'''Функция для вывода повторяющихся элементов из двух списков.
На вход подаются два списка, которые необходимо сравнить.'''
def cross (fruit1, fruit2):
    return [x for x in fruit1 if x in fruit2]
print (cross (['яблоко', 'груша', 'арбуз', 'манго'], ['манго','абрикос', 'вишня', 'яблоко', 'арбуз', 'мандарин']))

# Задание-3:
# Дан список, заполненный произвольными числами.
# Получить список из элементов исходного, удовлетворяющих следующим условиям:
# + Элемент кратен 3
# + Элемент положительный
# + Элемент не кратен 4

'''Функция генерирует список случайных цисел.
На вход подается три значения: ("начало диапазона", "конец диапазона", "кол-во чисел"'''
def myrand (a, b, c):
    import random
    lst = []
    for _ in range(c):
        lst.append(random.randint(a,b))
    return lst

'''Функция фильтрует список чисел так, чтобы остались значения соответствующие критериям:
- Элемент кратен 3
- Элемент положительный
- Элемент не кратен 4
На вход подается список чисел.'''
def filter (lst):
    return [i for i in lst if (i > 0 and (i / 3) % 1 == 0 and (i / 4) % 1 != 0)]

print (filter(myrand (-100 ,100 ,30)))