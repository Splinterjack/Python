# Задача-3:
# Дан произвольный список из целых чисел.
# Получите НОВЫЙ список из элементов исходного, выполнив следующие условия:
# если элемент кратен двум, то разделить его на 4, если не кратен, то умножить на два.

a = [43,2,546,38,5,9,26,325,7,8,7]  #Дан произвольный список
i = 0
b = []
while i in range (len (a)-1):       #Сортируем элементы на четные и нечетные и выполняем преобразование
    if a[i] % 2 == 1:
        b.append (a[i] * 2)
    else:
        b.append(a[i] / 4)
    i+=1
print (b)                           #Выводим переработанный список