# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне.

# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз

# Подсказка: воспользоваться методом .format()

fruit = ["яблоко", "банан", "киви", "арбуз"]            #Дан список фруктов
print ('Дано:', fruit)
n = 0
print ('Результат:')                                    #Форматирую список
while len(fruit) > n:
    print (n+1,'.', '{:>7}'.format(fruit[n]))
    n += 1

